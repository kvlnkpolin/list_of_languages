def bubble_sort(arr):
    n = len(arr)  # Получаем длину массива
    
    # Проходим по всем элементам массива
    for i in range(n):
        swapped = False  # Флаг для отслеживания обменов на этом проходе
        
        # Последние i элементов уже на своих местах
        for j in range(0, n - i - 1):
            # Сравниваем соседние элементы
            if arr[j] > arr[j + 1]:
                # Меняем элементы местами, если они в неправильном порядке
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True  # Устанавливаем флаг обмена
        
        # Если на этом проходе не было обменов, массив отсортирован
        if not swapped:
            break  # Выходим из цикла досрочно

def print_array(arr):
    # Выводим все элементы массива через пробел
    print(" ".join(map(str, arr)))

if __name__ == "__main__":
    arr = [64, 34, 25, 12, 22, 11, 90]  # Создаем тестовый массив
    
    print("Исходный массив:", end=" ")
    print_array(arr)  # Выводим исходный массив
    
    bubble_sort(arr)  # Сортируем массив
    
    print("Отсортированный массив:", end=" ")
    print_array(arr)  # Выводим отсортированный массив
